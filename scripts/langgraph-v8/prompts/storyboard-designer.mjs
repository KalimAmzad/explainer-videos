/**
 * v8 Storyboard Designer prompt — the creative director.
 *
 * Takes content plan + theme and produces a visual blueprint:
 *   - Assets to fetch (icons + AI images)
 *   - Content blocks with text + colors
 *   - Animation choreography hints
 *   - Bottom bar takeaway
 *
 * NO layout decisions — the scene coder owns spatial design.
 */

export function buildStoryboardPrompt({ researchNotes, theme }) {
  const scenes = researchNotes.scenes || [];

  return `You are the creative director for a motion-graphics studio. Take this content plan and produce a **visual storyboard** — specifying WHAT to show and HOW to animate, but NOT where to position things (the coder will handle layout).

## Theme
\`\`\`json
${JSON.stringify(theme, null, 2)}
\`\`\`

## Content Plan (${scenes.length} scenes)
\`\`\`json
${JSON.stringify(scenes.map(s => ({
  scene_number: s.scene_number,
  title: s.title,
  subtitle: s.subtitle || '',
  key_concept: s.key_concept,
  teaching_points: s.teaching_points,
  visual_idea: s.visual_idea,
  duration: s.duration,
})), null, 2)}
\`\`\`

---

## Asset Types

### Icons (\`type: "icon"\`)
- Downloaded from Icons8 API
- \`search_term\`: simple English, 1-2 words ("brain", "clock", "rocket")
- \`platform\`: "color" (vivid) or "ios17" (clean line)
- \`size\`: 128 or 256

### AI Images (\`type: "ai_image"\`)
- Generated by Gemini. Max 1 per scene.
- \`prompt\`: detailed description. Include: "dark background (#0f172a)", "minimalist", "flat illustration", "no text"

---

## Output Format

Return ONLY this JSON (no fences, no explanation):

{
  "scenes": [
    {
      "scene_number": 1,
      "title": "Scene Title",
      "subtitle": "Short subtitle",
      "visual_direction": "Creative brief for the coder — describe the feel, mood, key visual hierarchy, what should dominate the scene",
      "assets": [
        { "id": "s1_brain", "type": "icon", "search_term": "brain", "platform": "color", "size": 256 },
        { "id": "s1_hero", "type": "ai_image", "prompt": "minimalist illustration of..." }
      ],
      "content_blocks": [
        { "type": "teaching_point", "text": "Exact text", "color": "accent1", "icon_ref": "s1_brain" },
        { "type": "stat", "value": "40%", "label": "of daily actions are habits", "color": "primary" }
      ],
      "animations": {
        "title": { "type": "wipe_reveal", "delay_sec": 0 },
        "subtitle": { "type": "wipe_reveal", "delay_sec": 0.35 },
        "content_blocks": { "type": "stagger_spring", "stagger_sec": 0.4 },
        "hero": { "type": "fade_scale", "delay_sec": 0.8 },
        "bottom_bar": { "type": "fade_up", "delay_pct": 0.72 }
      },
      "bottom_bar": {
        "emoji": "💡",
        "text": "Key takeaway text"
      }
    }
  ]
}

## Rules

1. **Asset IDs**: prefix with scene number: \`s{N}_name\` (snake_case). E.g. \`s1_brain\`, \`s2_loop_hero\`.
2. **Content blocks**: Use EXACT teaching_points text. Types: \`teaching_point\` (text + icon), \`stat\` (big number + label), \`quote\`.
3. **Colors**: Use palette keys: \`primary\`, \`secondary\`, \`accent1\`, \`accent2\`. Rotate for variety.
4. **Animation timing**: \`delay_sec\` = seconds from scene start. \`stagger_sec\` = gap between items. \`delay_pct\` = % of narration.
5. **Bottom bar**: EVERY scene must have one.
6. **Icons**: 2–4 per scene. Simple search terms.
7. **AI images**: Max 1 per scene. For hero illustrations only.
8. **visual_direction**: Describe the MOOD and HIERARCHY — what dominates, what supports, what's the visual story. Don't specify pixel positions.
9. **Stat opportunities**: Numbers/percentages → \`stat\` block for visual prominence.

Return ONLY the JSON.`;
}
