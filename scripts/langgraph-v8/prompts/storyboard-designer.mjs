/**
 * v8 Storyboard Designer prompt — the creative director.
 *
 * Takes content plan + theme and produces a complete visual blueprint
 * that downstream nodes execute deterministically.
 */

export function buildStoryboardPrompt({ researchNotes, theme }) {
  const scenes = researchNotes.scenes || [];

  return `You are the creative director at a world-class motion design studio (Kurzgesagt × MasterClass × Linear.app). Your job is to take an educational content plan and produce a **complete visual storyboard** — a precise blueprint that a coder can implement without making any creative decisions.

## Your Input

### Theme
\`\`\`json
${JSON.stringify(theme, null, 2)}
\`\`\`

### Content Plan (${scenes.length} scenes)
\`\`\`json
${JSON.stringify(scenes.map(s => ({
  scene_number: s.scene_number,
  title: s.title,
  subtitle: s.subtitle || '',
  key_concept: s.key_concept,
  teaching_points: s.teaching_points,
  visual_idea: s.visual_idea,
  duration: s.duration,
})), null, 2)}
\`\`\`

---

## Layout Catalog

Choose ONE layout per scene from this catalog:

| Layout | Best For | Structure |
|--------|----------|-----------|
| \`split\` | Most versatile — teaching points + visual | Title top-left, cards left (y:200–570), hero/icons right (x:700+), bottom bar |
| \`grid-3\` | Three equal concepts | Title top, 3 icon cards in a row below |
| \`grid-4\` | Four concepts or steps | Title top, 2×2 grid of cards below |
| \`stacked-list\` | Sequential steps, processes | Title top, numbered vertical list filling left panel |
| \`centered-hero\` | One big idea, visual metaphor | Title top-left, oversized image/stat centered |
| \`comparison\` | Before/after, two sides | Title top, two equal columns below |
| \`process-flow\` | Linear flow, cause-effect | Title top, horizontal flow with arrows |

**\`split\` is the default — use it unless another layout clearly fits better.**

---

## Animation Types

Assign one animation per element from this catalog:

| Type | Use For | Effect |
|------|---------|--------|
| \`wipe_reveal\` | Titles, subtitles, text | Left-to-right clip-path reveal |
| \`fade_scale\` | Hero images, large visuals | Fade in + scale from 0.7→1.0 |
| \`spring_slide\` | Cards, list items, blocks | Spring translateY + opacity, with direction |
| \`pop\` | Stats, numbers, icons | Scale overshoot (0→1.1→1.0) |
| \`fade_in\` | Backgrounds, overlays | Simple opacity 0→1 |
| \`fade_up\` | Bottom bars, footers | Slide up + fade from bottom |

---

## Asset Types

For each visual element, specify an asset:

### Icons (\`type: "icon"\`)
- Downloaded from Icons8 via HTTP API
- Specify: \`search_term\` (simple English, 1-2 words), \`platform\` ("color" for vivid, "ios17" for clean), \`size\` (128 or 256)
- **search_term must be simple**: "brain", "clock", "checkmark", "rocket", "target", "gear", "chart"

### AI Images (\`type: "ai_image"\`)
- Generated by Gemini image model
- Specify: \`prompt\` (detailed description — style, content, mood, background)
- Use sparingly (max 1 per scene) — for hero illustrations only
- Prompt tips: "minimalist", "flat illustration", "dark background", "no text"

---

## Output Format

Return ONLY this JSON (no markdown fences, no explanation):

{
  "scenes": [
    {
      "scene_number": 1,
      "layout": "split",
      "title": "Scene Title",
      "subtitle": "Short subtitle",
      "visual_direction": "Brief description of overall scene look",
      "assets": [
        {
          "id": "s1_brain",
          "type": "icon",
          "search_term": "brain",
          "platform": "color",
          "size": 256
        },
        {
          "id": "s1_hero",
          "type": "ai_image",
          "prompt": "minimalist illustration of neural pathways forming a loop, dark background, glowing blue nodes, modern flat style"
        }
      ],
      "content_blocks": [
        {
          "type": "teaching_point",
          "text": "Exact text from content plan",
          "color": "accent1",
          "icon_ref": "s1_brain"
        },
        {
          "type": "stat",
          "value": "40%",
          "label": "of daily actions are habits",
          "color": "primary"
        }
      ],
      "animations": {
        "title": { "type": "wipe_reveal", "delay_sec": 0 },
        "subtitle": { "type": "wipe_reveal", "delay_sec": 0.4 },
        "content_blocks": { "type": "stagger_spring", "stagger_sec": 0.4, "direction": "up" },
        "right_panel": { "type": "fade_scale", "delay_sec": 0.8 },
        "bottom_bar": { "type": "fade_up", "delay_pct": 0.7 }
      },
      "bottom_bar": {
        "emoji": "💡",
        "text": "Key takeaway text"
      }
    }
  ]
}

---

## Rules

1. **Asset IDs**: Always prefix with scene number: \`s{N}_name\`. Use snake_case. Examples: \`s1_brain\`, \`s2_step1_icon\`, \`s3_hero\`.

2. **Content blocks**: Use the EXACT teaching_points text from the content plan. Choose the right type:
   - \`teaching_point\` — text card with optional icon reference
   - \`stat\` — large number with context label
   - \`quote\` — attributed quote or key phrase

3. **Color assignments**: Use palette key names: \`primary\`, \`secondary\`, \`accent1\`, \`accent2\`, \`text\`. Rotate colors across teaching points for visual variety.

4. **Animation timing**:
   - \`delay_sec\`: absolute delay from scene start (e.g., 0, 0.4, 0.8, 1.2)
   - \`delay_pct\`: percentage of narration duration (0.7 = enters at 70% through)
   - \`stagger_sec\`: time between consecutive items in a group

5. **Bottom bar**: Every scene MUST have a bottom bar with emoji + takeaway text.

6. **Icons per scene**: 2–5 icons per scene. Use diverse search terms. Platform "color" for vivid icons.

7. **AI images**: Max 1 per scene. Only use when a strong visual metaphor needs illustration.

8. **Scene variety**: Alternate layouts across scenes. Don't use the same layout for consecutive scenes.

9. **Stat opportunities**: If the content mentions numbers/percentages, create a \`stat\` content_block to make it visually prominent.

10. **Icon references**: Each teaching_point can optionally reference an icon via \`icon_ref\` matching an asset \`id\`.

Return ONLY the JSON. No markdown fences.`;
}
